# 元组的基本知识
# 元组中的元素不可变

# 元组的三种创建
tuple1 = ("c语言", "Java", "python", "c++")
tuple2 = ("高级语言", ("Python", "Java"), "中级语言", "汇编语言",)
tuple3 = "计算机组成原理", "计算机网络", "数据结构"

# 如果元组只有一个元素，则元组定义时需要添加一个","
tuple4 = ("Python_exec",)
# 定义了一个字符串
tuple5 = ("Python_exec")

# 可以使用type()来测试变量类型
print("tuple4的类型为:", type(tuple4))
print("tuple5的类型为:", type(tuple5))




