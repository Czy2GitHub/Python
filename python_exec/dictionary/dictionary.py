# 字典 相当于Java、c++中的Map对象
# 字典中键不可变 键必须唯一 只能通过键读取而不能通过索引读取 字典是任意对象无序的集合
# 字典可变 且可以人任意嵌套

# dictionary = {'key1':'value1, 'key2':'value2'}
# dictionary:表示字典名称
# key1、key2···keyn:表示元素的键 必须唯一
# value: 表示元素的值，可以是任何数据类型，不是必须唯一
dictionary = {'animals': 'Cat', 'fruit': 'peach',}
print(dictionary)

